# 📘 Description:
#     This program prints the first 20 even numbers starting from 0.
#     It uses a simple for-loop and avoids hardcoding each number.
# ------------------------------------------------------

def main():
    print("🔢 First 20 Even Numbers:\n")
    
    # 🔁 Loop runs 20 times (from 0 to 19)
    for i in range(20):
        even_number = i * 2  # 🧠 Multiply by 2 to get even numbers
        print(even_number)

# ------------------------------------------------------
# ▶️ Start the Program
# ------------------------------------------------------
if __name__ == '__main__':
    main()

