# 📘 Description:
#     This program counts down from 10 to 1 and then prints "Liftoff!" 🎉✨
# ------------------------------------------------------

def main():
    # 🔁 Countdown from 10 to 1 (inclusive)
    for i in range(10, 0, -1):  # Start=10, Stop=0 (non-inclusive), Step=-1
        print(f"⏳ {i}")

    # 🚀 After countdown
    print("🚀 Liftoff! 🌟🔥")

# ------------------------------------------------------
# ▶️ Start the Program
# ------------------------------------------------------
if __name__ == '__main__':
    main()

