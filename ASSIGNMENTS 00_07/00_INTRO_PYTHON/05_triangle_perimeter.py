def main():
    # ➡️ User se side 1 ka length poochna (float type) 🦾
    side1: float = float(input("What is the length of side 1? "))

    # ➡️ User se side 2 ka length poochna (float type) 🦿
    side2: float = float(input("What is the length of side 2? "))

    # ➡️ User se side 3 ka length poochna (float type) 🦵
    side3: float = float(input("What is the length of side 3? "))

    # ➡️ Perimeter calculate karna (teeno sides ka sum) ➕
    perimeter = side1 + side2 + side3

    # 🖨️ Result print karna with celebration emoji 🎉
    print(f"🧮 The perimeter of the triangle is {perimeter} 🎉")


# ------------------------------------------------------
# Program start point 🚀
# ------------------------------------------------------
if __name__ == '__main__':
    main()

