# -------------------------------------
# ➕ Program to add two numbers entered by the user
# -------------------------------------
def main():
    print("👋 Welcome! This program adds two numbers.")

    # Prompt user to enter the first number and convert to int
    first_number = int(input("🔢 Enter the first number: "))

    # Prompt user to enter the second number and convert to int
    second_number = int(input("🔢 Enter the second number: "))

    # Calculate the sum of the two numbers
    total_sum = first_number + second_number

    # Display the result with a friendly message
    print(f"✅ The sum of {first_number} and {second_number} is: {total_sum}")

# ------------------------------------------------------
# Program entry point
# ------------------------------------------------------
if __name__ == "__main__":
    main()

