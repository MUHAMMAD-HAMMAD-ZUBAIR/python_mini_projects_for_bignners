def main():
    print("🔢 First 20 Even Numbers:")
    # Loop 20 times, from 0 to 19
    for i in range(20):
        even_number = i * 2  # Calculate even number
        print(even_number)   # Print the even number

# Call the main function when script runs
if __name__ == '__main__':
    main()
