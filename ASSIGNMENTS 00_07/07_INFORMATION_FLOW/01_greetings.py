# -------------------------------------
# 🙋‍♂️ 1. Function to return a greeting
# -------------------------------------
def greet(name):
    return "👋 Greetings " + name + "! 😊"

# -------------------------------------
# 📥 2. Main function to get input and greet
# -------------------------------------
def main():
    name = input("📝 What's your name? ")  # Ask user for their name
    print(greet(name))  # Call greet() to show greeting

# -------------------------------------
# 🚀 Start the program
# -------------------------------------
if __name__ == '__main__':
    main()

